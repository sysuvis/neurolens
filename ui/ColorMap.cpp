vec4f ColorMap::getLinearColor( const float& v, const LinearColorMapType& color_map ){
	switch (color_map){
		case COLOR_MAP_GRAY_SCALE:
			return getGrayScale(v);
		case COLOR_MAP_PURPLE_SCALE:
			return getPurpleScale(v);
		case COLOR_MAP_RAINBOW:
			return getRainbowColor(v);
		case COLOR_MAP_PERCEPTUAL:
			return getPerceptualColor(v);
		case COLOR_MAP_SPECTRUAL:
			return getSpectrualColor(v);
		case COLOR_MAP_PRGN:
			return getBrewerColorGnPr(v);
		case COLOR_MAP_GYRD:
			return getBrewerColorGyRd(v);
		case COLOR_MAP_PIYG:
			return getBrewerColorGYPi(v);
		case COLOR_MAP_YIORRD:
			return getBrewerColorYlOrRd(v);
		case COLOR_MAP_D3:
			return getD3Color((int)v);
		case COLOR_MAP_D3_NO_GRAY:
			return getD3ColorNoGray((int)v);
		case COLOR_MAP_YELLOW_SCALE:
			return getYellowScale(v);
		case COLOR_MAP_BY_NAME:
			return getColorByName((ColorName)((int)v));
	}
}

void ColorMap::getColorMap(std::vector<vec4f>& ret_color_map, const LinearColorMapType& map_name){
	ret_color_map.clear();
	switch (map_name){
		case COLOR_MAP_GRAY_SCALE:
			ret_color_map.assign(gray_scale_color_map, gray_scale_color_map+GRAY_SCALE_COLOR_NUM); break;
		case COLOR_MAP_PURPLE_SCALE:
			ret_color_map.assign(purple_scale_color_map, purple_scale_color_map+PURPLE_SCALE_COLOR_NUM); break;
		case COLOR_MAP_RAINBOW:
			break;
		case COLOR_MAP_PERCEPTUAL:
			ret_color_map.assign(perceptual_color_map, perceptual_color_map+PERCEPTUAL_COLOR_NUM); break;
		case COLOR_MAP_SPECTRUAL:
			ret_color_map.assign(spectrual_color_map, spectrual_color_map+GRAY_SCALE_COLOR_NUM); break;
		case COLOR_MAP_PRGN:
			ret_color_map.assign(color_brewer_gnpr_map, color_brewer_gnpr_map+COLOR_BREWER_GNPR_NUM); break;
		case COLOR_MAP_GYRD:
			ret_color_map.assign(color_brewer_gyrd_map, color_brewer_gyrd_map+COLOR_BREWER_GYRD_NUM); break;
		case COLOR_MAP_PIYG:
			ret_color_map.assign(color_brewer_gypi_map, color_brewer_gypi_map+COLOR_BREWER_GYPI_NUM); break;
		case COLOR_MAP_YIORRD:
			ret_color_map.assign(color_brewer_ylorrd_map, color_brewer_ylorrd_map+COLOR_BREWER_YLORRD_NUM); break;
		case COLOR_MAP_D3:
			ret_color_map.assign(d3_color_map, d3_color_map+D3_COLOR_NUM); break;
		case COLOR_MAP_D3_NO_GRAY:
			ret_color_map.assign(d3_color_map_no_gray, d3_color_map_no_gray+D3_COLOR_NUM_2); break;
		case COLOR_MAP_YELLOW_SCALE:
			ret_color_map.assign(yellow_scale_color_map, yellow_scale_color_map + YELLOW_SCALE_COLOR_NUM); break;
	}
}

