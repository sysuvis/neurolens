vec4f interpoloate_color(vec4f* cmap, const int& num, float v){
	v = clamp(v, 0.000001f, 0.999999f);
	v *= (num-1);
	int iv = (int)v;
	float f=v-iv;
	vec4f ret = cmap[iv]*(1.0f-f)+cmap[iv+1]*f;
	return ret;
}

