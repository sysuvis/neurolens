vec4f ColorMap::getRainbowColor(float v){
	v = clamp(v, 0.0f, 1.0f);
	vec4f color;
	if(v<0.25f){
		color.x = 0.0f;
		color.y = v*4.0f;
		color.z = 1.0f;
	} else if (v<0.5f){
		color.x = 0.0f;
		color.y = 1.0f;
		color.z = (0.5f-v)*4.0f;
	} else if (v<0.75f){
		color.x = (v-0.5f)*4.0f;
		color.y = 1.0f;
		color.z = 0.0f;
	} else {
		color.x = 1.0f;
		color.y = (1.0f-v)*4.0f;
		color.z = 0.0f;
	}
	color.w = 1.0f;
	return color;
}

