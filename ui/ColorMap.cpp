std::string ColorMap::toHexString(const float& f) {
	unsigned int i = f * 255;
	std::string ret = "00";
	ret[0] += ((i >> 4) > 9) ? ((i >> 4) + 39) : (i >> 4);
	ret[1] += ((i & 0xf) > 9) ? ((i & 0xf) + 39) : (i & 0xf);
	return ret;
}

std::string ColorMap::toHexString(const vec4f& color) {
	std::string ret = "\"#" + toHexString(color.r);
	ret += toHexString(color.g);
	ret += toHexString(color.b);
	ret += "\"";
	return ret;
}

std::string ColorMap::toHexString(const LinearColorMapType& color_map)
{
	std::vector<vec4f> colors;
	getColorMap(colors, color_map);
	if (colors.empty()) return "";

	std::string ret;
	for (auto& c : colors) {
		ret += toHexString(c) + ",";
	}
	ret.resize(ret.size() - 1);
	return ret;
}

